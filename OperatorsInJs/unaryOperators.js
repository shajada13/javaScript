//  (++)operators
a = 2 ;
b = 3 ;

a++;

console.log ("a = ", a); // increment a previous line , then print ; ( ans is 3 );
console.log ("b = ", b++); // this time b is not increment , the ans is 3 , because it this operators(b++) used in the same line 

// but if we used this 
c = 4 ;
console.log ("c = " , ++ c) // the ans is 5 , 1st increment c , and then print ;





//  (--)operators


a = 2 ;
b = 3 ;

a--;

console.log ("a = ", a); // decrement a previous line , then print ; ( ans is 1);
console.log ("b = ", b--); // this time b is not decrement , the ans is 3, because it this operators(b--) used in the same line 

// but if we used this 
c = 4 ;
console.log ("c = " , -- c) // the ans is 3, 1st decrement c , and then print ;